// Extract unique values for filters
export const getUniqueLocations = () => {
  return [...new Set(photos.map(photo => photo.country))].sort()
}

export const getUniqueCategories = () => {
  return [...new Set(photos.map(photo => photo.category))].sort()
}

export const getUniqueYears = () => {
  return [...new Set(photos.map(photo => photo.year))].sort((a, b) => b - a)
}

